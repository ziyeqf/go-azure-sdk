func TestNewConnectorID(t *testing.T) {
	id := NewConnectorID("12345678-1234-9876-4563-123456789012", "example-resource-group", "storageAccountName", "connectorName")

	if id.SubscriptionId != "12345678-1234-9876-4563-123456789012" {
		t.Fatalf("Expected %q but got %q for Segment 'SubscriptionId'", id.SubscriptionId, "12345678-1234-9876-4563-123456789012")
	}

	if id.ResourceGroupName != "example-resource-group" {
		t.Fatalf("Expected %q but got %q for Segment 'ResourceGroupName'", id.ResourceGroupName, "example-resource-group")
	}

	if id.StorageAccountName != "storageAccountName" {
		t.Fatalf("Expected %q but got %q for Segment 'StorageAccountName'", id.StorageAccountName, "storageAccountName")
	}

	if id.ConnectorName != "connectorName" {
		t.Fatalf("Expected %q but got %q for Segment 'ConnectorName'", id.ConnectorName, "connectorName")
	}
}

func TestFormatConnectorID(t *testing.T) {
	actual := NewConnectorID("12345678-1234-9876-4563-123456789012", "example-resource-group", "storageAccountName", "connectorName").ID()
	expected := "/subscriptions/12345678-1234-9876-4563-123456789012/resourceGroups/example-resource-group/providers/Microsoft.Storage/storageAccounts/storageAccountName/connectors/connectorName"
	if actual != expected {
		t.Fatalf("Expected the Formatted ID to be %q but got %q", expected, actual)
	}
}

