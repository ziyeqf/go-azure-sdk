var _ resourceids.ResourceId = &VirtualNetworkApplianceId{}

func TestNewVirtualNetworkApplianceID(t *testing.T) {
	id := NewVirtualNetworkApplianceID("12345678-1234-9876-4563-123456789012", "example-resource-group", "virtualNetworkApplianceName")

	if id.SubscriptionId != "12345678-1234-9876-4563-123456789012" {
		t.Fatalf("Expected %q but got %q for Segment 'SubscriptionId'", id.SubscriptionId, "12345678-1234-9876-4563-123456789012")
	}

	if id.ResourceGroupName != "example-resource-group" {
		t.Fatalf("Expected %q but got %q for Segment 'ResourceGroupName'", id.ResourceGroupName, "example-resource-group")
	}

	if id.VirtualNetworkApplianceName != "virtualNetworkApplianceName" {
		t.Fatalf("Expected %q but got %q for Segment 'VirtualNetworkApplianceName'", id.VirtualNetworkApplianceName, "virtualNetworkApplianceName")
	}
}

func TestFormatVirtualNetworkApplianceID(t *testing.T) {
	actual := NewVirtualNetworkApplianceID("12345678-1234-9876-4563-123456789012", "example-resource-group", "virtualNetworkApplianceName").ID()
	expected := "/subscriptions/12345678-1234-9876-4563-123456789012/resourceGroups/example-resource-group/providers/Microsoft.Network/virtualNetworkAppliances/virtualNetworkApplianceName"
	if actual != expected {
		t.Fatalf("Expected the Formatted ID to be %q but got %q", expected, actual)
	}
}

